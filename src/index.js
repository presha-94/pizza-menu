function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Pizza Dizza Pizzeria</h1>
    </header>
  );
}
function Pizza({ pizzaObj }) {
  // if (pizzaObj.soldOut) return null;
  return (
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObj.photoName} alt="" />
      <div>
        <p>{pizzaObj.name}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}
function Menu() {
  const numPizza = pizzaData.length;
  return (
    <main className="menu">
      <h2>whats in the Menu!!</h2>
      {numPizza > 0 ? (
        <>
          <p>
            Exotic Italian cuisnes to just satisfy your tastebuds !!!! Its time
            for you to have some spicy time ...!!!
          </p>
          <ul className="pizzas">
            {pizzaData.map((pizza) => (
              <Pizza pizzaObj={pizza} key={pizza.name} />
            ))}
          </ul>
        </>
      ) : (
        "mmmmm"
      )}
    </main>
  );
}
function Order({ closeHour }) {
  return (
    <div className="order">
      <p>
        We are open until{closeHour}to oder please visit or try our online
        service
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 9;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;
  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} />
      ) : (
        <p>
          We are glad to serve you between {openHour}:00 until {closeHour}:00 of
          the day
        </p>
      )}
    </footer>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
